/** 품종명으로 꿀팁 조회 — 매칭 규칙 팁 + 부족분은 공통 팁으로 채움(최대 5개). */
export function getBreedTips(species: Species, breedKo?: string | null): BreedTip[] {
  const out: BreedTip[] = [];
  const seen = new Set<string>();
  const push = (t: BreedTip) => { if (!seen.has(t.title)) { seen.add(t.title); out.push(t); } };
  if (breedKo) {
    for (const r of RULES) {
      if (r.species && r.species !== species) continue;
      if (r.match.test(breedKo)) r.tips.forEach(push);
    }
  }
  GENERIC_TIPS[species].forEach(push);
  return out.slice(0, 5);
}

/** careAdvisor 프롬프트 주입용 — 유료 리포트에 꿀팁이 실제로 포함되게. */
export function tipsToPrompt(species: Species, breedKo?: string | null): string {
  const tips = getBreedTips(species, breedKo);
  if (tips.length === 0) return '';
  const lines = tips.map((t) => `- ${t.title}: ${t.body}`).join('\n');
  return `\n\n[검증된 품종 꿀팁 — 리포트의 관련 섹션(그루밍/운동/음식/케어)에 자연스럽게 녹여서 반드시 포함하라]\n${lines}`;
}
